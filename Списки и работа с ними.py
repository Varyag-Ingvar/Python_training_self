spisok_1 = [2,18,25,4,21]
spisok_1.insert(0, 300) # вставляет по индексу на 0 позицию число 300, остальные элементы смещаются вправо
print(spisok_1)

spisok_2 = [3,5,15,18,24]
spizjeno = spisok_2.pop() # pop по умолчанию вытаскивает последний элемент списка и его можно сохранить в переменную
                          # например, тут мы сохранили в переменную spizjeno посл.элемент списка spisok_2
                          # при этом из списка вытаскиваемый элемент удаляется (типо команды вырезать)!!!
print(spisok_2)
print(spizjeno)
print(spisok_2.pop(2))    # либо извлекаем элемент по индексу, тут мы тырим 15 (индекс 2 в списке)
spisok_2.remove(5)        # удаляет элемент списка по значению (5 удаляется)
print(spisok_2)

