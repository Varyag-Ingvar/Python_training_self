temp_august = [39,33,35,37,32,36]
print(temp_august)

print(38 in temp_august)
print(min(temp_august)) # минимальное значение в списке
print(max(temp_august)) # максимальное значение
print(sum(temp_august)) # сумма элементов списка
print(sorted(temp_august)) # сортирует по возрастанию
print(sorted(temp_august, reverse=True)) # сортирует по убыванию
print(sum(temp_august)/len(temp_august)) # среднее арифметическое (сумма всех элементов разделенная на их кол-во)

a = [43,54,2,54,32]
